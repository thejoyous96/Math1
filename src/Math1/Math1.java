package Math1;

public class Math1 {
	public static void main(String[] args) {
		int x = 10;
		int y = 5; 
		
		int sum = x + y;
		int sub = x - y;
		int mul = x * y; 
		int div = x / y;
		
		System.out.println("The sum of the two numbers is: " + sum);
		System.out.println("");
		System.out.println("When you subtract the two numbers, you get: " + sub);
		System.out.println("");
		System.out.println("After you multiply the two number, the result is: " + mul);
		System.out.println("");
		System.out.println("After diving the two numbers, the result is: " + div);
	}
}
